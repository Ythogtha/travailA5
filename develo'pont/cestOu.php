            <p><b>Un peu d'histoire</b></p>
            
            <p>Le pont : au départ c'est un passage étroit jeté entre deux rochers qui permet (point d'entrée) l'accès aux plateaux du massif du Vercors, et de traverser le torrent de la Bourne dont les gorges sont abruptes et très étroites. La première citation désigne un certain pont Chochignon (relire abbé Fillet, et S. Chaussamy). Le pont est étroit, tout au plus trois mètres, et son entrée démarre à la hauteur de la maison Thomas sur l'actuel Pont Picard (on ignore l'origine de ce nom, qui est donc moderne ; on parle aussi du gouffre Picard, creux dans la Bourne profond d'une dizaine de mètres à peu près sous le pont).
              
                <img class="imagepont1800" src="../img/ponten1800.jpg" alt="image pont en royans en 1800">


            <p>Au XIXe siècle le Pont est élargi. La route de Villard est créée en 1872, la route de sainte Eulalie actuelle est créée elle aussi avant la fin du XIXe siècle, et la circulation des charrois de troncs de bois provenant de la Route des Goulets entraine un besoin d'élargissement. De nombreuses maisons sont abattues (il y avait avant trois fois plus de maisons suspendues au-dessus du gouffre qu'au XXIe siècle).</p>

            <p>Pont-en-Royans est un bourg touristique riche en histoire. On apprend aux archives du Dauphiné qu'il y a eu trois châteaux : Le premier château dans l'appellation topographique ancienne correspond au bourg fortifié. Le deuxième château à l'actuel vestige au-dessus de la "montagne trois châteaux". Le troisième château des XVIIe et XVIIIe siècles fut détruit lors d'un bombardement en 1945 et correspond à l'emplacement du collège. Ses remparts, dont il ne reste que de rares vestiges, incluent « La porte de France », « la porte Agnès », « la porte de Villeneuve », et « la porte du Merle ». C’est là que s’arrêtaient les limites du bourg en des temps anciens, et si les toits de pierres blanches ont disparu, le village et ses maisons suspendues, accrochées au rocher, attirent toujours les touristes en été sur les bords de la rivière, la Bourne.</p>
                
                <img class="imagemaisonsuspendu" src="../img/maisonsuspendu.jpg" alt="image maison suspendu de pont en royans">


            <p>Au XIXe siècle, ce bourg fut sous Napoléon III plus connu que la ville de Valence, pourtant proche. Sa notoriété dépassa largement les frontières de France, grâce aux routes creusées dans la roche karstique du Vercors (Un exploit pour l'époque qui contribua largement à la renommée du savoir-faire du génie français, lors des toutes premières expositions universelles à Paris).</p>


            <P>Pont-en-Royans fut aussi l'une des trois premières villes (pour le nombre d'habitants) électrifiées de France. La conduite d'eau forcée passe encore sous la ville (un musée de l'eau permet de la découvrir).</P>


            <p>On fabriquait encore dans les années 1950 du matériel électrique pour l'habitat : interrupteurs, douilles... (À l'origine tous ces équipements étaient tournés en bois de buis* dans lesquels on plaçait le système en cuivre).</p>

    
            <p>Le tournage sur bois de buis fut longtemps une des activités principales de la ville.</p>


            <p>On redécouvre de nos jours dans de très nombreux livres des XIXe et XXe siècles, les premières photos sur plaques en "verre photographique lumière" qui représentent les maisons suspendues de Pont-en-Royans et les routes du Vercors (routes des Grands Goulets, route des gorges de la Bourne, Pont de la Goule Noire..).</p>


            <p>Pendant la Seconde Guerre mondiale la place du Pont Picard fut l'une des plaques tournantes des mouvements résistants.</p>
        
